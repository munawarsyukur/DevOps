package main

import "fmt"

func main() {
	// Declare a variable
	var age int

	// Assign a value to the variable
	age = 25

	// Print the variable
	fmt.Println("Age:", age)

	// Declare and initialize a variable in one line
	name := "Munawar"
	fmt.Println("Name:", name)

	// You can change the value of a variable later
	name = "Bob"
	fmt.Println("Updated Name:", name)
}
